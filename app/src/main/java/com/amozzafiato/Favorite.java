package com.amozzafiato;

public class Favorite {
    private String nameCar;
    private String image;

    public Favorite(String nameCar, String image) {
        this.nameCar = nameCar;
        this.image = image;
    }

    public String getNameCar() {
        return nameCar;
    }

    public String getImage() {
        return image;
    }
}
